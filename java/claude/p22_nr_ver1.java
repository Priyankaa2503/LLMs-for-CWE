// Java has no preprocessor. The closest equivalent is the 'import'
// statement, which makes classes from other packages available.
// System.out.println() lives in java.lang.System — and java.lang is
// automatically imported by the compiler, so no explicit import is needed.

public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello, World!");
    }
}