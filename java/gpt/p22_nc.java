/**
 * p22_nc.java - Minimal program demonstrating imports like C directives
 *
 * The import mirrors a C preprocessor include by declaring dependencies
 * before compilation.
 */
import java.io.PrintStream;

public class p22_nc {
    public static void main(String[] args) {
        PrintStream out = System.out;
        out.println("Printing is working via java.io.PrintStream import.");
    }
}

