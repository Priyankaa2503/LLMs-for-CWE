// JavaScript has no preprocessor stage.
// The modern equivalent of #include is the ES Module 'import' statement,
// resolved by the JS engine at load time — not before compilation.
//
// console.log() is a global built-in in all JS environments — no import needed.
//   → Equivalent of #include <stdio.h>  : not required
//   → Equivalent of #include <stdlib.h> : not required for basic output

console.log("Hello, World!");